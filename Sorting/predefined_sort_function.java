package Sorting;
import java.util.Arrays;

public class predefined_sort_function {
    
    public static void main(String[] args) {
        int a[]={11,2,7,45,9,56};
        char b[]={'a','g','f','b','z'};
        Arrays.sort(a);
        Arrays.sort(b);
        System.out.println(Arrays.toString(a));
        System.out.println(Arrays.toString(b));
    }
}